终于来到了这个困难的板块！你精心筹划好了约会项目、穿上了最有品味的衣服，打理好了清爽的发型，做好一切心理准备，终于，你们在一家咖啡馆里相见了！一切的进展仿佛不能再顺利的时候，看着对面姣好的面容，你的大脑陷入了宕机，话语在嘴边盘旋却迟迟不能说出口，就连一个问好都无比艰难——在经历了半个小时的尴尬和冷场后，女生说临时有事匆匆离开了，然后再也没有回复你的消息......显然我们不想遇到如此窘迫的境况，但是又不得不承认想在约会中通过交流有所进展确实是一件不那么容易的事，千头万绪一把抓，结果皆从指缝漏，那么这里我将给大家列出一个清晰、明确的聊天流程，让你做到胸有成竹、有的放矢。**1.初次见面**：聊天的前五分钟，可以从天气、交通等话题开始，简单的建立一些熟悉感。“hello，你怎么过来的”我看着对方落座，一边微笑点头示意，一边发出简单的询问。“打车过来的，最近有点堵，所以晚到了会儿”坏消息是她迟到了，好消息是她有向我解释迟到的原因。“周末这边确实比较堵，你平时不常过来吗？”没必要抓住她迟到的事情斤斤计较，不如聊聊为什么她没预料到会迟到。“挺少的，偶尔逛街会来”她此刻已经落座，恢复平静心情后，开始打量坐在她面前的这个人。到此为止，我们完成了初步的开场对话，当然你们聊的内容肯定不至于此，我非常提倡大家用自己跳跃的思维将话题带入天马行空的方向，你还记得自己刷的短视频吗？如果反复刷到同一类型的短视频，你会感觉疲劳，但是如果每一个视频都出乎你的意料，你的好奇感会催使着你不断下滑——聊天也是这样！源源不断的新话题会刺激人脑对于未知的渴望**2.渐入佳境**：接下来的目标，就是要将话题转移到两个人的身上，了解彼此的喜好经历并建立联系。现在，她已经透露出了自己的一些生活轨迹“较少来商圈、有逛街的经历”，你更倾向于回复她什么呢？相信大家都会有自己想说的话，想聊的话题，在约会中，没有哪一句话是至关重要、决定生死的，这里我给大家提供三种话题引导的思路：A.向下联想：抓住细节，不断深挖更多信息。抓住关键词[少]——“那你通常是怎么安排时间的？/你逛街一般会去哪些店？”B.平行联想：过度话题，寻找其他方向。“哦那确实不好预测交通情况，我周末还是比较喜欢做XX事，你了解XX吗？”C.向上联想：升华提炼，找到人物性格。“看你朋友圈还挺宅的，听你这么说，像是个i人”在当前话题有很多内容可以拓展延伸时，使用向下联想；在当前话题进入穷尽时，使用平行联想；在双方兴致高时，使用向上联想，本条优先级最高。通过话题的联想，你们可以了解到彼此的喜好、性格、过去经历**3.突破瓶颈**：相信聊天聊的愉快大家努力一下都是可以做到的，但是如何将这种愉快转化为良好的暧昧氛围呢？别忘了，你为什么要来约会！约会的目的是“察看对方是否可以成为我的另一半”——所以既然你能够和对方交谈甚欢，那么为什么不去把主题回归到我们的最开始的初心上呢？所以，在这里我要提出一个核心态度：无需求好感。我经常看到大家说冲塔冲塔，那什么是冲塔？冲塔就是说：我喜欢你，现在也需要你喜欢我。毫无疑问，这么冲你不死谁死。因为你的喜欢是有条件的、绑架的。而我这里提到的无需求，则表达的是“即便你不喜欢我，对我没有任何感觉，也不影响我喜欢你，这是独立的两件事”。当然，很多人会说，做不到，道理都懂之类的，那我想告诉你的是，问题在于你选择的时机不对——在关系的末期，投入了大量情绪、精力沉没成本的你，已经没有办法正确决策了，这些东西会干扰你的思绪，你对对方的每个行为的期望都不是基于当下一刻，而是基于过去全部事件的累积和，所以女生最多说的一句话是“压力太大”。而在关系的初期，没有那么多投入，当对方表现出让你心动的时刻，为什么不能单纯的赞美一句呢？“你今天的穿搭真的很棒！我很喜欢”“你竟然有这么好的习惯！我真的很欣赏自律的人”看，当对方表现出优秀的品质时，我不相信你是不心动的，但是大多数人都会在心里默默加分，然后冲塔。那么我今天教给你最重要的一课就是，学会在关系的前中期，就要真诚、直接的表达你的心意。大家喜欢“直球”恋爱的核心，并不是结果趋向于一夜情，而是这样的模式表达出来的好感是无需求的！如果你能做到这一点，那么恭喜你，你可能会被拒绝，但是被拒绝的原因绝对不会再是好人卡、男闺蜜、好兄弟、错过窗口等......**总结：正确的约会聊天流程，三种聊天方法，直接的好感表达，这都是你披荆斩棘的利器！**

七、兴趣指标判断

