我从不否认大家的勇敢和真诚，但是面对着喜欢的人，想要顺利判断出对方的态度确实不是一件容易的事，至少绝大多数人不敢更进一步的原因是“因为不知道对方是什么想法，担心自己被拒绝/冒犯对方”说起来，这是不是很像游戏？当敌人的位置处于未知时，我们的决策畏首畏尾，而当我们清楚知晓对方的踪迹时，我们往往变得果断又坚决！一些经验丰富的人，会通过蛛丝马迹判断出对方的大致态度，然后不断试探最终大胆推进，那么在这里，我将会通过一些小技巧来让你们获得这种能力——在学习技巧前，我们需要明确一个概念，叫做**“假性社交”**：出于某些客观原因/主观顾虑，即便对方不具备社交的意向，却仍然会进入你所期待的社交状态中。举一些栗子：大领导的查户口问询、过年亲戚聊天等。在约会中，女性比起男性是更倾向于和谐、友善的，所以在尴尬的氛围下，她们往往会主动打破僵局，提高自己的状态，释放亲和的信号，不过要注意，此刻并不是她真正的对你好感爆棚！而是进入了“假性社交”，不要搞错咯～对于判断对方的态度，我们从两个方面入手：语言、行为。语言方面，将所有的聊天内容抽象概括，解析对方背后的态度，可以得到三个核心要素：**自我披露程度、认可程度、刁难程度。自我披露程度**：在聊天过程中，对方是否愿意提及自己过去较为隐私的经历、秘密，且交流必须是中性或正面积极的。**认可程度**：在你描述自己的经历、行为时，对方是否会使用明确的褒义形容词夸赞，是否对我们表达的观念持有正面态度。**刁难程度**：对方是否会出现抬杠、质疑、打压、调侃等降低你价值的行为。如何理解这三个元素呢？自我披露程度揭示了对方此刻对你是否信任，是否认为你是安全的。自我披露程度越高，则表明对方此刻与你的关系已经摆脱陌生阶段，并且对方开始向你寻求更多的认可，更愿意与你进行观念和思想的交换。认可程度揭示了对方此刻对你是否存在社会、人格层面上的认可，是否认为你是特殊的。认可程度越高，则表明对方此刻认为你已经高过了身边其他同样异性，对于她来说你的存在是有意义的，更愿意与你的生活产生更多交集。刁难程度揭示了对方此刻对你是否存在误解、恶意、不安全感，是否认为你是有预谋的、不怀好意的、虚伪的。刁难程度越高，则表明对方捕捉到了你言行中刻意、古怪之处，对于她来说你是别有用心的，你会通过花言巧语伪装自己的行为，她更想戳破你的伪装得知你背后的真实目的。在行为方面，我们需要关注的点并不多，我也会使用更有依据的方式帮助你来进行判断。在所有的行为中，为了便于记忆和使用，我选取最直接也是最容易发现的三个点：眼神、语调、身体朝向对于眼神，你需要保证的是，在对方进行表达的时刻，你的眼神是要和对方的眼神保持链接的，而在你进行语言输出的时候，如果对方同样保持了眼神的注视，那恭喜你，这是一个再好不过的消息了！对于语调，如果对方的语气是偏向下沉、平稳，那说明此刻的话题，或者此刻的你并不能引起她的兴趣，而对方的语气如果是上扬的，更多使用了疑问句，或者用疑问、反问表达了肯定时，那说明此刻的你正在释放魅力！对于身体朝向，当你们面对面时，如果对方的身体是后倾、紧缩的，那么说明你需要转移话题或者聊一些轻松的内容了；如果对方身体前倾，双手立在桌上，那么无须质疑，她的全部精力已经交给了你！**总结：对于判断对方的兴趣程度，从语言和行为上皆有办法，但是需要如何修正自己的行为，才是重中之重。**

八、关系进挪

