相信对于突破关系，表白选择很多人都存在疑惑，全靠直觉和本能，冲塔说冲就冲，结局全靠天定。对于关系的推进，其实存在两个关键的质变环节。第一个环节，是在你们接触的初期。当你们的交流逐渐变多、氛围逐渐融洽时，你需要敲响警钟，因为如果不做任何干预，没有前提的社交会向朋友方向转变，所以在这个环节，需要你适当表达好感，点到即止，暗示这段关系的发展方向即可。第二个环节，也是最重要的环节，就是在约会后，大家往往感觉到了关系融洽，但是却不知道如何更进一步，想牵手但是犹犹豫豫。作为公开发布的教学内容，我将科普一些基础概念，请各位自行思考。在心理学中，存在这样一个概念，认知失调：一般来说，人的态度与行为相协调，不需要改态度与行为。若两者不一致，如做了与态度相违背的事，或没做想做的事，这时就产生了认知失调。行为和认知的抵触，认知失调会给我们带来紧张和不安，我们会尽力解除这种紧张，以重新恢复平衡。为了接触认知失调，往往会做的有：1.改变态度2.增加认知3.减少选择，让自己相信，自己之所以会做出与态度相矛盾的事，是因自己自己别无选择4.改变行为，使自己的行为原先的态度一致(思路解析教学移步楼主1月答疑帖16楼[https://ngabbs.com/read.php?tid=38972563)](https://ngabbs.com/read.php?tid=38972563))所以这就是我一直告诉大家，先牵手，不表白。至于标题中的接吻，既然牵手成功，自然是水到渠成的内容，可不能说我是标题党哦。

